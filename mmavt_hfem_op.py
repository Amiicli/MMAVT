def generate_unique_name(nameToUse:str,properties:MMAVT_mbody):
    goofyName = nameToUse + str(1)
    for prop in properties:
        if prop.name == goofyName:
            newName = generate_unique_name(goofyName,properties)
            return newName
    return goofyName
